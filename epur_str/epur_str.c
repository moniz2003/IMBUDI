#include <unistd.h>

int	main(int ac, char *av[])
{
	int teste = 0, i = 0;

	if (ac == 2)
	{
		while (av[1][i] == ' ' || av[1][i] == '\t')
			i++;
		while (av[1][i] != '\0')
		{
			if (av[1][i] == ' ' || av[1][i] == '\t')
				teste = 42;
			if (!(av[1][i] == ' ' ) && !(av[1][i] == '\t'))
			{
				if (teste == 42)
					write (1, " ", 1);
				write (1, &av[1][i], 1);
				teste = 0;
			}
			i++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
